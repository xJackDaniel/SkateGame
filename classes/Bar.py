class Bar:
    def __init__(self, type, height, width, jump):
        self.type = type
        self.height = height
        self.width = width
        self.jump = jump

    def get_type(self):
        return self.type

    def get_height(self):
        return self.height

    def get_width(self):
        return self.width

    def get_jump(self):
        return self.jump
